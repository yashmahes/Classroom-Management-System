    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String classroom = jComboBox1.getSelectedItem().toString();
        
        Classroom my_classroom = null;
        for(int i=0; i<WelcomeScreen.classroom_list.size(); i++)
        {
            Classroom temp = WelcomeScreen.classroom_list.get(i);
            if(temp.name.equals(classroom))
            {
                my_classroom = temp;
                break;
            }
        }
        
        if(my_classroom == null)
        {
            JOptionPane.showMessageDialog(rootPane, "Please select Classroom");  
            return;
        }
        
        String result = "Name: " + my_classroom.name + "\n"
                + "Location: " + my_classroom.location + "\n"
                + "maximum_number_of_students: " + my_classroom.maximum_number_of_students + "\n"
                + "Assigned teacher: " + my_classroom.assignedTeachers + "\n"
                + "Students: " + my_classroom.students + "\n";
        
        
        jTextArea1.setText(result);
    }//GEN-LAST:event_jButton1ActionPerformed

